// ─── Registry helpers ──────────────────────────────────────────────────────────
export const getTuneFileById = (id: string): TuneFile | undefined =>
  tuneFiles.find((f) => f.id === id)

export const getTuneFilesByFilter = (params: {
  romVersion?: string
  fuel?: string
  stage?: string
  turboType?: string
}): TuneFile[] =>
  tuneFiles.filter((f) => {
    if (params.romVersion && f.romVersion !== params.romVersion) return false
    if (params.fuel && f.fuel !== params.fuel) return false
    if (params.stage && f.stage !== params.stage) return false
    if (params.turboType && f.turboType !== params.turboType) return false
    return true
  })
